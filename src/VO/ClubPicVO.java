package vo;

public class ClubPicVO {
	String serial =null;
	String picture = null;
	String clubSerial =null;
	public String getSerial() {
		return serial;
	}
	public void setSerial(String serial) {
		this.serial = serial;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public String getClubSerial() {
		return clubSerial;
	}
	public void setClubSerial(String clubSerial) {
		this.clubSerial = clubSerial;
	}

}
